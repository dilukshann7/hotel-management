    public void loadRevenueData() {
        // Past week
        int[] week = revenueService.getRevenue(7);
        weekcus.setText(String.valueOf(week[0]));
        weekrev.setText(String.valueOf(week[1]));

        // Past month
        int[] month = revenueService.getRevenue(30);
        monthcus.setText(String.valueOf(month[0]));
        monthrev.setText(String.valueOf(month[1]));

        // Past year
        int[] year = revenueService.getRevenue(365);
        yearcus.setText(String.valueOf(year[0]));
        yearrev.setText(String.valueOf(year[1]));
    }

    private class RevenueService {

        public int[] getRevenue(int days) {
            int[] result = new int[2]; // [customerCount, revenue]

            String query = "SELECT COUNT(customerName) AS CustomerCount, " +
                    "SUM(DATEDIFF(DAY, CONVERT(date, customerCheckinDate, 103), " +
                    "CONVERT(date, customerCheckoutDate, 103)) * r.price) AS Revenue " +
                    "FROM CustomerOld c " +
                    "JOIN Rooms r ON c.roomNo = r.roomNo " +
                    "WHERE CONVERT(date, customerCheckinDate, 103) >= DATEADD(DAY, -" + days + ", GETDATE())";

            try (ResultSet rs = Select.getData(query)) {
                if (rs != null && rs.next()) {
                    result[0] = rs.getInt("CustomerCount");
                    result[1] = rs.getInt("Revenue");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error fetching revenue data: " + e.getMessage());
            }

            return result;
        }
    }

